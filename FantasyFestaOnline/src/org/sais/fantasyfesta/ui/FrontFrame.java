    void initModel() {

        if (FTool.readConfig("semiautoupdate") != null) {
            if (FTool.readConfig("semiautoupdate").equals("off")) {
                semiautoUpdate = false;
            } else {
                semiautoUpdate = true;
            }
        }

        if (FTool.readConfig("playbgm") != null) {
            if (FTool.readConfig("playbgm").equals("true")) {
                playBGM = true;
            } else {
                playBGM = false;
            }
        }

        if (FTool.readConfig("background") != null) {
            if (FTool.readConfig("background").equals("true")) {
                background = true;
            } else {
                background = false;
            }
        }


        if (FTool.readConfig("externbgmplayer").equals("true")) {
            externBGMPlayer = true;
        } else {
            externBGMPlayer = false;
        }


        if (FTool.readConfig("externbgmplayerpath") != null) {
            externBGMPlayerPath = FTool.readConfig("externbgmplayerpath");
        }
        if (FTool.readConfig("externbgmplayerpath") != null) {
            fontname = FTool.readConfig("font");

        }
        if (FTool.readConfig("customizedicon") != null) {
            if (FTool.readConfig("customizedicon").equals("true")) {
                customizedIcon = true;
            } else {
                customizedIcon = false;
            }
        }
        if (FTool.readConfig("alpha") != null && !FTool.readConfig("alpha").equals("")) {
            alpha = Float.parseFloat(FTool.readConfig("alpha"));
            if (Float.isNaN(alpha) || alpha > 1.0) {
                alpha = 1.0f;
            }
        }


        language = FTool.readConfig("language");
        country = FTool.readConfig("country");
        if (!((language.equals("")) || (language == null))) {
            locale = new Locale(language, country);
        }
        FTool.setLocale();

        BackgroundManager.loadImagePaths();
        BackgroundManager.loadMusicPaths();
    }

