    private void fontButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_fontButtonActionPerformed
        // TODO add your handling code here:
        GraphicsEnvironment ge = GraphicsEnvironment.getLocalGraphicsEnvironment();

        JList fontNames = new JList(ge.getAvailableFontFamilyNames());

        int response = JOptionPane.showConfirmDialog(this, new JScrollPane(fontNames));
        Font font = new Font(fontNames.getSelectedValue().toString(), 0, 12);
        if (response == JOptionPane.YES_OPTION && font != null) {
            System.out.println(font);
            FTool.setMyFont(container, font);
            fontname = font.getName();
        }
    }//GEN-LAST:event_fontButtonActionPerformed

