   private void cnameActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cnameActionPerformed
       try {
           for (int i = 0; i < 2000; ++i) {
               if (((ListItem) (clist.getModel().getElementAt(i))).label.getInfo().getName().contains(cname.getText())) {
                   clist.setSelectedIndex(i);
                   clist.ensureIndexIsVisible(i);
                   break;
               }
           }
       } catch (ArrayIndexOutOfBoundsException ex) {
           ex.printStackTrace();
           JOptionPane.showMessageDialog(this, "No Such Card!");
       }
       cname.setSelectionStart(0);
       cname.setSelectionEnd(cname.getText().length());
   }//GEN-LAST:event_cnameActionPerformed

   private void cnumActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cnumActionPerformed
       if (cnum.getText().toLowerCase().startsWith("v0")) {
           cnum.setText("99" + cnum.getText().substring(2));
       }
       try {
           for (int i = 0; i < 2000; ++i) {
               if (((ListItem) (clist.getModel().getElementAt(i))).label.getInfo().isNo(FTool.safeParseInt(cnum.getText()))) {
                   clist.setSelectedIndex(i);
                   clist.ensureIndexIsVisible(i);
                   break;
               }
           }
       } catch (ArrayIndexOutOfBoundsException ex) {
           ex.printStackTrace();
           JOptionPane.showMessageDialog(this, "No Such Card!");
       }
       cnum.setSelectionStart(0);
       cnum.setSelectionEnd(4);
   }//GEN-LAST:event_cnumActionPerformed

