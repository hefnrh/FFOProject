    private void loadList() {
        DefaultListModel mod = new DefaultListModel();
        Collection<CardInfo> cards = CardDatabase.listCards();
        ListItem[] litem = new ListItem[cards.size()];
        mod.removeAllElements();
        int i = 0;
        for (CardInfo info : cards) {
            litem[i] = new ListItem(info.getBackgroundColor(), info.getName(), new UniLabel(null, info.createCard()));
            mod.addElement(litem[i]);
            ++i;
        }
        clist.setModel(mod);
        clist.setCellRenderer(new MyCellRenderer());
        clist.addMouseListener(new java.awt.event.MouseAdapter() {

            @Override
            public void mouseClicked(java.awt.event.MouseEvent evt) {
                int index = clist.locationToIndex(evt.getPoint());
                FTool.showCard(((ListItem) (clist.getModel().getElementAt(index))).label.getInfo(), SCArea);
            }

            @Override
            public void mouseEntered(java.awt.event.MouseEvent evt) {
                if (moveandshow.isSelected()) {
                    int index = clist.locationToIndex(evt.getPoint());
                    FTool.showCard(((ListItem) (clist.getModel().getElementAt(index))).label.getInfo(), SCArea);
                }
            }
        });
        clist.addMouseMotionListener(new java.awt.event.MouseAdapter() {

            @Override
            public void mouseMoved(java.awt.event.MouseEvent evt) {
                if (moveandshow.isSelected()) {
                    int index = clist.locationToIndex(evt.getPoint());
                    FTool.showCard(((ListItem) (clist.getModel().getElementAt(index))).label.getInfo(), SCArea);
                }
            }
        });
    }

