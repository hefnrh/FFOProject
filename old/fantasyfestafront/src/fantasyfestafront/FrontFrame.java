    void initModel() {

        if (Tools.readConfig("semiautoupdate").equals("on")) {
            semiautoUpdate = true;
        } else {
            semiautoUpdate = false;
        }

        if (Tools.readConfig("playbgm").equals("true")) {
            playBGM = true;
        } else {
            playBGM = false;
        }
        if (Tools.readConfig("externbgmplayer").equals("true")) {
           
            externBGMPlayer = true;
        } else {
            externBGMPlayer = false;
        }

        if (Tools.readConfig("externbgmplayerpath") != null) {
            externBGMPlayerPath = Tools.readConfig("externbgmplayerpath");
        }

        if (Tools.readConfig("customizedicon").equals("true")) {
            customizedIcon = true;
        } else {
            customizedIcon = false;
        }

        language = Tools.readConfig("language");
        country = Tools.readConfig("country");
        if (!((language.equals("")) || (language == null))) {


            locale = new Locale(language, country);

        }

        Tools.loadBGM(sceneName, BGMPath);


    }

    private void setMyFont(Component Comp) {
        Font myFont = new Font(java.util.ResourceBundle.getBundle("fantasyfestafront/Bundle").getString("FrontFrame.default.font"), Font.PLAIN, 12);
        if (Comp != null) {
            try {
                Comp.setFont(myFont);
            } catch (Exception e) {
                return;
            }
        }
        if (Comp instanceof Container) {
            Component[] components = ((Container) Comp).getComponents();
            for (int i = 0; i < components.length; i++) {
                Component child = components[i];
                if (child != null) {
                    //System.out.println(child.getClass().getName());
                    setMyFont(child);
                }
            }
        }
        return;
    }

