    private void chooseBGMPlayer(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_chooseBGMPlayer
        // TODO add your handling code here:
        File file = new File(externBGMPlayerField.getText());
        if (file.exists()) {
            jFileChooser1.setCurrentDirectory(file);
        }
        int result = jFileChooser1.showOpenDialog(null);
        if (result == JFileChooser.CANCEL_OPTION) {
            return;
        }

        externBGMPlayerField.setText(jFileChooser1.getSelectedFile().getPath());


    }//GEN-LAST:event_chooseBGMPlayer

